@router.get("/users", response_model=List[UserOut])
def list_users(current_user: TokenData = Depends(RoleChecker(["ADMIN"]))):
    """Admin retrieves a list of all users."""
    pool = get_db_pool()
    users_list = []
    with pool.get_connection() as conn:
        with conn.cursor() as cursor:
            cursor.execute(
                "SELECT user_id, username, role, is_active, created_at, doctor_id FROM users ORDER BY username"
            )
            for row in cursor.fetchall():
                users_list.append(
                    UserOut(
                        user_id=row[0],
                        username=row[1],
                        role=row[2],
                        is_active=bool(row[3]),
                        created_at=row[4],
                        doctor_id=row[5]
                    )
                )
    return users_list